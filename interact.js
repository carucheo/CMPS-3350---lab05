//function created to retrieve input and output translation
function translateSentence() {
    //collects inputed value from user using textarea
    const userInput = document.getElementById('sentenceInput').value;
    const translationOutput = document.getElementById('translationOutput');
    
//split user input by white space to ensure each word is individually matched
    const words = userInput.split(' ');

//convrt to lowercase and look for match in dictionary
    const translatedWords = words.map(word => {
        const lowerCaseWord = word.toLowerCase();
        return dictionary[lowerCaseWord] || word; 
    });
    
//join translated words back together into single string
    translationOutput.innerText = `Translation: ${translatedWords.join(' ')}`;
}

